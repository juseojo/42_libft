int	ft_isprint(int c)
{
	if ((39 < c && c < 48) || (49 < c && c < 58) || (59 < c && c < 68) 
			|| (69 < c && c < 78) || (99 < c && c < 108) || (109 < c && c < 118)
			|| (119 < c && c < 128) || (129 < c && c < 138) || (139 < c && c < 148)
			|| (149 < c && c < 158))
	{
		return (1);
	}
	return (0);
}
